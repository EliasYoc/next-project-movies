import type { Metadata } from "next";
import { AppRouterCacheProvider } from "@mui/material-nextjs/v15-appRouter";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { ConfigStoreProvider } from "./_providers/config-store-provider";
import { getTmdbConfiguration } from "./_services/tmdb/configuration";
import LayoutHeader from "./LayoutHeader";
import { ThemeProvider } from "@mui/material";
import muiTheme from "./muiTheme";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const configData = await getTmdbConfiguration({ which: "details" });

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <AppRouterCacheProvider>
          <ThemeProvider theme={muiTheme}>
            <LayoutHeader />
            <ConfigStoreProvider value={configData}>
              <main>{children}</main>
            </ConfigStoreProvider>
          </ThemeProvider>
        </AppRouterCacheProvider>
      </body>
    </html>
  );
}
